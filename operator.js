var a=20;
var b=10;

// Arithmetic operators
console.log("a + b = "+(a+b));
console.log("a - b = "+(a-b));
console.log("a * b = "+(a*b));
console.log("a / b = "+(a/b));
console.log("a % b = "+(a%b));
console.log("a ** b = "+(a**b));
console.log("a++ = "+(a++));
console.log("a-- = "+(a--));


// Comparison operators
console.log("a > b = "+(a>b));
console.log("a < b = "+(a<b));
console.log("a >= b = "+(a>=b));
console.log("a <= b = "+(a<=b));
console.log("a != b = "+(a!=b));
console.log("a == b = "+(a==b));
console.log("a === b = "+(a===b));  //Strict equal (value + type)

// Logical operators
console.log("(a>10 && b>20) = "+(a>10 && b>20));
console.log("(a>10 || b>20) = "+(a>10 || b>20));
console.log("(!(a>10)) = "+!(a>10));

// Ternary operator
console.log("Condition?True:False;"+(a==b?true:false));

